import {Directive, Output, EventEmitter, Input} from '@angular/core';

import { BaseMapDirective } from './base-map-directive';
import { Ng2MapComponent } from '../components/ng2-map.component';

const INPUTS = [
  'options',
  'circleOptions', 'drawingControl', 'drawingControlOptions', 'drawingMode',
  'map', 'markerOptions', 'polygonOptions', 'polylineOptions', 'rectangleOptions'
];
const OUTPUTS = [
  'circlecomplete', 'markercomplete', 'overlaycomplete',
  'polygoncomplete', 'polylinecomplete', 'rectanglecomplete'
];

@Directive({
  selector: 'ng2-map > drawing-manager',
})
export class DrawingManager extends BaseMapDirective {
  @Output() public circlecomplete: EventEmitter<any> = new EventEmitter(); // generated by ngc-pre-compiler.js
  @Output() public markercomplete: EventEmitter<any> = new EventEmitter(); // generated by ngc-pre-compiler.js
  @Output() public overlaycomplete: EventEmitter<any> = new EventEmitter(); // generated by ngc-pre-compiler.js
  @Output() public polygoncomplete: EventEmitter<any> = new EventEmitter(); // generated by ngc-pre-compiler.js
  @Output() public polylinecomplete: EventEmitter<any> = new EventEmitter(); // generated by ngc-pre-compiler.js
  @Output() public rectanglecomplete: EventEmitter<any> = new EventEmitter(); // generated by ngc-pre-compiler.js
  @Output() public initialized$: EventEmitter<any> = new EventEmitter();

  @Input() public options: any; // generated by ngc-pre-compiler.js
  @Input() public circleOptions: any; // generated by ngc-pre-compiler.js
  @Input() public drawingControl: any; // generated by ngc-pre-compiler.js
  @Input() public drawingControlOptions: any; // generated by ngc-pre-compiler.js
  @Input() public drawingMode: any; // generated by ngc-pre-compiler.js
  @Input() public map: any; // generated by ngc-pre-compiler.js
  @Input() public markerOptions: any; // generated by ngc-pre-compiler.js
  @Input() public polygonOptions: any; // generated by ngc-pre-compiler.js
  @Input() public polylineOptions: any; // generated by ngc-pre-compiler.js
  @Input() public rectangleOptions: any; // generated by ngc-pre-compiler.js

  constructor(ng2MapComp: Ng2MapComponent) {
    super(ng2MapComp, 'DrawingManager', INPUTS, OUTPUTS);
    this.libraryName = 'drawing';
  }
}
